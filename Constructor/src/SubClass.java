
public class SubClass {

	private String ad ;
	private int id;
	public SubClass(String ad, int id) {
		this.ad = ad;
		this.id = id;
	}
	public String getAd() {
		return ad;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	

}
