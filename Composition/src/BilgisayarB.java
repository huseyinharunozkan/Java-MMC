
public class BilgisayarB {
	
	private MonitorB monitor;
	
	private KasaB kasa;
	
	private AnakartB anakart;

	public BilgisayarB(MonitorB monitor, KasaB kasa, AnakartB anakart) {
		this.monitor = monitor;
		this.kasa = kasa;
		this.anakart = anakart;
	}

	public MonitorB getMonitor() {
		return monitor;
	}

	public void setMonitor(MonitorB monitor) {
		this.monitor = monitor;
	}

	public KasaB getKasa() {
		return kasa;
	}

	public void setKasa(KasaB kasa) {
		this.kasa = kasa;
	}

	public AnakartB getAnakart() {
		return anakart;
	}

	public void setAnakart(AnakartB anakart) {
		this.anakart = anakart;
	}
	
	

}
